def tip_of_day(now: datetime) -> str:
    return TIPS[now.isocalendar()[1] % len(TIPS)]


def header(now: datetime) -> str:
    DAYS   = ["Понедельник", "Вторник", "Среда", "Четверг", "Пятница", "Суббота", "Воскресенье"]
    PHASES = {
        3: "🏗 Март — «Фундамент»",
        4: "📦 Апрель — «Объём»",
        5: "✨ Май — «Рельеф»",
        6: "🏖 Июнь — «Финиш»",
    }
    phase_label = PHASES.get(now.month, "🏋️ Тренировка")
    return (
        f"🗓 *{DAYS[now.weekday()]}, {now.strftime('%d.%m.%Y')}*\n"
        f"📌 {phase_label}\n"
        f"{'─' * 32}\n"
    )


